# Minimum and Maximum built in fucntions

playerOneScore = 10
playerTwoScore = 7

# Minimum
print(min(playerOneScore, playerTwoScore))
print(min(3, 32, 14, 8, 0, 56, -3, 99))

# A-Z -> 0-26
print(min('Kathryn', 'Katie'))
print(min('Angela', 'Bob'))

# Maximum
print(max(playerOneScore, playerTwoScore))
print(max(3, 32, 14, 8, 0, 56, -3, 99))

# A-Z -> 0-26
print(max('Kathryn', 'Katie'))
print(max('Angela', 'Bob'))