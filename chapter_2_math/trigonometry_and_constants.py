# Math Module
import math

# Constants
print(math.pi) # Constant pi, the ratio of the circumference of a circle to its diameter.
print(math.e) # Constant e, the base of the natural logarithm.
print(math.nan) # Not a number (NaN), the result of invalid or undefined mathematical operations.
print(math.inf) # Infinity, the result of infinite mathematical operations.
print(-math.inf) # Negative infinity, the negative result of infinite mathematical operations.

# Trigonometry
obst_direction = math.cos(math.pi / 4) # The cosine of π/4 (45 degrees).
print(obst_direction)
print(math.sin(math.pi / 4)) # Calculates and prints the sine of π/4 (45 degrees).